    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         if(jTable1.getSelectedRow() !=-1){
            
           int id=Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(),0).toString());
           stage= stageDao.findObjectById(id);
           stageDao.deleteObject(stage);
           
           stageModel.setData(stageDao.DisplayAllObject());
            jTable1.setModel(stageModel);
             
         }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int select= jTable1.getSelectedRow();
        if(select !=-1){
            Stage s = stageDao.findObjectById(Integer.parseInt((String) jTable1.getModel().getValueAt(select,0)));
            jTextField1.setText(s.getNomEntreprise());
            jTextField2.setText(s.getDomaine());
            jTextField3.setText(s.getDuree());
            dateCoursBox.setDate(s.getDate());
            jTextArea1.setText(s.getDescription());
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int select = jTable1.getSelectedRow();
        if (select != -1) {
            stage = stageDao.findObjectById(Integer.parseInt((String) jTable1.getModel().getValueAt(select, 0)));

            stage.setNomEntreprise(jTextField1.getText());
            stage.setDomaine(jTextField2.getText());
            stage.setDuree(jTextField3.getText());
            java.sql.Date sqlDate = null;
            java.util.Date utilDate;

            if (dateCoursBox.getDate() != null) {

                utilDate = dateCoursBox.getDate();
                sqlDate = new java.sql.Date(utilDate.getTime());
                stage.setDate(sqlDate);

            }
            stage.setDescription(jTextArea1.getText());
            stageDao.updateObject(stage);
            stageModel.setData(stageDao.DisplayAllObjectById());
            jTable1.setModel(stageModel);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

