    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (CoursTableModel.getSelectedRow() != -1) {
            Evaluation eval = new Evaluation();
            EvaluationDAO evDAO = new EvaluationDAO();
            Etudiant etu = new Etudiant();
            EtudiantDAO etuDAO = new EtudiantDAO();
            etu = etuDAO.findObjectById(Authentification.idPersonneAutentfie);
            eval.setEtudiant(etu);
            //Cours cours = new Cours();
            // cours = (Cours) CoursTableModel.getValueAt(CoursTableModel.getSelectedRow(), 0);
            int id = Integer.parseInt(CoursTableModel.getValueAt(CoursTableModel.getSelectedRow(), 0).toString());

            eval.setCommentaire(commentaireText.getText());
            eval.setNote(Integer.parseInt(jTextField1.getText()));
            java.util.Date utilDate = new Date();

            java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());

            eval.setDate(sqlDate);
            evDAO.insertObject(eval, id);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

